const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let str = ''
   for (let i = 0; i < expr.length; i+=10) {
    const podstroka = expr.slice(i, 10 + i);
    let res = ''
    if (podstroka.includes('*')) {
        str += " "
    } else {
        res = podstroka.replaceAll('10', '.').replaceAll('11', '-').replaceAll('0', '');
        let letter = MORSE_TABLE[res];
        str += letter;
    }
   }
   return str;
}

module.exports = {
    decode
}